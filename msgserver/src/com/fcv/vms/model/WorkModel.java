package com.fcv.vms.model;

public class WorkModel {
	private WorkData workData;
	private WorkReg workReg;
	
	
	public WorkModel(WorkData data){
		this.workData = data;
	}
	public WorkModel(WorkReg reg){
		this.workReg = reg;
	}
	public WorkModel(WorkData data, WorkReg reg){
		this.workData = data;
		this.workReg = reg;
	}
	public WorkModel(){}
	
	public WorkData getData() {
		return workData;
	}
	public void setData(WorkData data) {
		this.workData = data;
	}
	public WorkReg getReg() {
		return workReg;
	}
	public void setReg(WorkReg reg) {
		this.workReg = reg;
	}

}




