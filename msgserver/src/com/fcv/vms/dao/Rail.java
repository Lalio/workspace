package com.fcv.vms.dao;

import java.io.Serializable;

import com.fcv.vms.Rail.RailUtil;

public class Rail implements Serializable {
	private Integer id;	
	private String railName;
	private String railLatLng;

	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getRailName() {
		return railName;
	}
	public void setRailName(String railName) {
		this.railName = railName;
	}
	public String getRailLatLng() {
		return railLatLng;
	}
	public void setRailLatLng(String railLatLng) {
		this.railLatLng = railLatLng;
			
	}
	
	
	private float[][] latlngs;
	public float[][] getLatlngs()
	{
		return latlngs;
	}
	public void setLatlngs(float[][] latlngs)
	{
		this.latlngs=latlngs;
		
	}
	
}
